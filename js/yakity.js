psyc.funky_text = function(m, templates) {
	var template = templates.get(m.method);
	var reg = /\[[\w-]+\]/g;
	var div = document.createElement("div");
	div.className = psyc.abbreviations(m.method).join(" ");
	
	var cb = function(result, m) {
		s = result[0].substr(1, result[0].length-2);
		var span = document.createElement("span");
		span.className = s;
		var a = s.split("-");
		s = a[0];
		var type;
		if (a.length > 1) {
			type = a[1];
			span.className = s+" "+type+" "+span.className;
		}
		var t;

		if (s == "data") {
			t = XSS.html_string_encode(m.data);
		} else if (s == "method") {
			t = XSS.html_string_encode(m.method);
		} else if (m.vars.hasIndex(s)) {
			var vtml = templates.get(s);
			t = m.vars.get(s);

			if (functionp(vtml)) {
				t = vtml.call(window, type, s, t, m);
			} else if (objectp(t)) {
				if (functionp(t.render)) {
					t = t.render(type);
				} else {
					t = t.toString();
				}
			}
		} else {
			span.className = "missing_variable";
			t = "["+s+"]";
		}

		if (objectp(t)) {
			span.appendChild(t);
		} else {
			span.appendChild(document.createTextNode(t));
		}

		return span;
	};

	var a = UTIL.split_replace(reg, template, cb, m);

	//meteor.debug("inserting "+a.length+" nodes");
	for (var i = 0; i < a.length; i++) {
		var t = a[i];
		if (stringp(t)) {
			if (t.length > 0) div.appendChild(document.createTextNode(t));
		} else {
			div.appendChild(t);
		}
	}

	return div;
};
psyc.Base = function() {
	this.msg = function (m) {
		var method = m.method;
		var none = 1;

		if (method.search("_") != 0) {
			if (meteor.debug) meteor.debug("Bad method "+method);
			return psyc.STOP;
		}

		for (var t = method; t; t = psyc.abbrev(t)) {
			if (functionp(this[t])) {
				none = 0;
				if (psyc.STOP == this[t].call(this, m)) {
					return psyc.STOP;
				}
			}
		}

		if (none && meteor.debug) {
			meteor.debug("No handler for "+method+" in "+this);	
		}
	};
	this.sendmsg = function(target, method, vars, data) {
		if (!vars) {
			vars = new psyc.Vars();
		}

		vars.set("_target", target);
		var m = new psyc.Message(method, vars, data);
		this.client.send(m);
	};
};
